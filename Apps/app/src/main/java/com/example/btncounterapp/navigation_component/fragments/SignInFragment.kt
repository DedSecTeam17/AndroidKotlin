                              savedInstanceState: Bundle?): View? {
        // Inflate the layout for this fragment

        val view = inflater.inflate(R.layout.fragment_sign_in, container, false)

        val  signInBtn = view.findViewById<Button>(R.id.sign_in_btn)
        val  signUpBtn = view.findViewById<Button>(R.id.sign_up_btn)

        signInBtn.setOnClickListener {
//            we will use nav direction
//            if there is an args
//            findNavController().popBackStack(R.id.sign_in_fragment,true)

            var action =    SignInFragmentDirections.actionSignInFragmentToHomeFragment()
